function dx = quad_nonlinear(t, x, U, P)
%QUAD_NONLINEAR  Права частина нелінійних рівнянь руху квадрокоптера.
%   dx = QUAD_NONLINEAR(t, x, U, P)
%
%   Реалізує рівняння з розділів 2 і 5 роботи T. Jiřinec (2011).
%
%   Вхід:
%     t  - час (не використовується напряму, потрібен для ode45)
%     x  - вектор стану (16x1):
%            x(1:3)   = x, y, z      положення (земна система NED) [м]
%            x(4:6)   = u, v, w      швидкість (зв'язана система)  [м/с]
%            x(7:9)   = phi,th,ps    кути Ейлера (крен,тангаж,рискання) [рад]
%            x(10:12) = p, q, r      кутові швидкості (зв'язана)   [рад/с]
%            x(13:16) = Om1..Om4     оберти гвинтів                [рад/с]
%     U  - збурення до обертів двигунів (4x1)                      [рад/с]
%     P  - структура параметрів (див. run_sim.m)
%
%   Вихід:
%     dx - похідна вектора стану (16x1)

% 1) Розпакування стану ---------------------------------------------------
u   = x(4);  v   = x(5);  w   = x(6);
phi = x(7);  th  = x(8);  ps  = x(9);
p   = x(10); q   = x(11); r   = x(12);
Om  = x(13:16);

% 2) Матриця напрямних косинусів D (земна -> зв'язана), формула 2.4 --------
D = [ cos(th)*cos(ps),                             cos(th)*sin(ps),                            -sin(th);
      sin(phi)*sin(th)*cos(ps)-cos(phi)*sin(ps),   sin(phi)*sin(th)*sin(ps)+cos(phi)*cos(ps),   sin(phi)*cos(th);
      cos(phi)*sin(th)*cos(ps)+sin(phi)*sin(ps),   cos(phi)*sin(th)*sin(ps)-sin(phi)*cos(ps),   cos(phi)*cos(th) ];

% 3) Кінематика положення (2.5): швидкість у земній системі = D' * [u v w] -
pos_dot = D.' * [u; v; w];

% 4) Кінематика кутів (2.8): E^-1 * [p q r] --------------------------------
Einv = [ 1, sin(phi)*tan(th),  cos(phi)*tan(th);
         0, cos(phi),         -sin(phi);
         0, sin(phi)/cos(th),  cos(phi)/cos(th) ];
ang_dot = Einv * [p; q; r];

% 5) Тяга та моменти (2.16, 2.26) -----------------------------------------
T  = P.b      * (Om(1)^2 + Om(2)^2 + Om(3)^2 + Om(4)^2);   % сумарна тяга
Mx = P.l*P.b  * (Om(2)^2 - Om(4)^2);                       % момент крену
My = P.l*P.b  * (Om(1)^2 - Om(3)^2);                       % момент тангажа
Mz = P.d      * (Om(2)^2 + Om(4)^2 - Om(1)^2 - Om(3)^2);   % момент рискання

% 6) Гіроскопічний момент гвинтів (розд. 2.3.1) ---------------------------
Hz = P.Ip * (Om(1) - Om(2) + Om(3) - Om(4));

% 7) Лінійні прискорення (2.17) -------------------------------------------
u_dot = r*v - q*w - P.g*sin(th);
v_dot = p*w - r*u + P.g*cos(th)*sin(phi);
w_dot = q*u - p*v + P.g*cos(phi)*cos(th) - T/P.m;

% 8) Кутові прискорення з гіроскопічними членами (2.33) -------------------
p_dot = Mx/P.Ix - q*r*(P.Iz-P.Iy)/P.Ix - Hz*q/P.Ix;
q_dot = My/P.Iy - p*r*(P.Ix-P.Iz)/P.Iy + Hz*p/P.Iy;
r_dot = Mz/P.Iz;

% 9) Динаміка двигунів (перший порядок): Om_dot = (Om_cmd - Om)/tau -------
Om_cmd = P.Omega0 + U(:);                                  % висіння + збурення
Om_cmd = min(max(Om_cmd, P.Omega_min), P.Omega_max);       % сатурація [рад/с]
Om_dot = (Om_cmd - Om) / P.tau;

% 10) Зібрати похідну вектора стану ---------------------------------------
dx = [ pos_dot; u_dot; v_dot; w_dot; ang_dot; p_dot; q_dot; r_dot; Om_dot ];
end
