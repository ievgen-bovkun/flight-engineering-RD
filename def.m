%% def.m
%  Файл параметрів (констант) нелінійної моделі квадрокоптера Linkquad.
%  Джерело значень: магістерська робота T. Jiřinec, 2011, розділи 4-6.
%  Запустіть цей файл (кнопка Run) — усі змінні з'являться у Workspace.
% -------------------------------------------------------------------------

g   = 9.81;        % прискорення вільного падіння             [м/с^2]

% --- Тензор інерції корпусу (розд. 4.2, формула 4.7) ----------------------
Ix  = 0.0093;      % момент інерції відносно осі крену         [кг*м^2]
Iy  = 0.0092;      % момент інерції відносно осі тангажа       [кг*м^2]
Iz  = 0.0151;      % момент інерції відносно осі рискання      [кг*м^2]

% --- Гвинт (розд. 4.1.1, формула 4.3) ------------------------------------
Ip  = 4.439e-5;    % момент інерції одного гвинта              [кг*м^2]

% --- Аеродинамічні коефіцієнти (розд. 4.3) -------------------------------
b   = 1.5108e-5;   % коефіцієнт тяги                           [кг*м]
d   = 3.0e-7;      % коефіцієнт опору (драг) - У РОБОТІ ЧИСЛА НЕМАЄ!
                   % Це орієнтовна оцінка. За потреби замініть на виміряне.
                   % Автор зазначає, що d - найменш точний параметр.

% --- Геометрія -----------------------------------------------------------
l   = 0.24;        % довжина плеча (центр -> гвинт) - ПІДСТАВТЕ СВОЄ [м]
                   % У роботі числа немає; 0.24 м - типова оцінка.

% --- Точка висіння (розд. 6.4) -------------------------------------------
Omega0 = 463.1;    % оберти кожного гвинта при висінні         [рад/с]

% --- Маса ----------------------------------------------------------------
% У роботі маса виміряна окремо, але точного числа немає.
% Обчислюємо її з умови висіння (тяга 4 гвинтів = вага), щоб точка
% висіння була ТОЧНОЮ рівновагою моделі:  m*g = 4*b*Omega0^2.
m   = 4*b*Omega0^2 / g;    % ~ 1.32 кг

% --- Динаміка двигуна: G(s) = Kdc/(tau*s + 1) (розд. 4.1.4, форм. 4.6) ----
tau = 0.1;         % стала часу двигуна                        [с]
Kdc = 0.7;         % статичний коефіцієнт підсилення            [-]

% --- Обмеження (сатурація) швидкості двигунів (розд. 5) -------------------
Omega_min = 0;     % мінімальні оберти                         [об/с]
Omega_max = 150;   % максимальні оберти                        [об/с]

fprintf('Параметри завантажено. Обчислена маса m = %.4f кг\n', m);
