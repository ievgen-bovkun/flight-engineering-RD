%% run_sim.m
%  Симуляція нелінійної моделі квадрокоптера та її реакції на збурення.
%
%  ЯК ЗАПУСТИТИ: покладіть def.m, quad_nonlinear.m і run_sim.m в одну папку,
%  зробіть її поточною в MATLAB, відкрийте цей файл і натисніть Run.
%
%  Порядок дій: 1) завантажити параметри  2) задати ступінчасте збурення
%               3) проінтегрувати рівняння  4) побудувати графіки
% -------------------------------------------------------------------------
clear; clc; close all;

def;                              % 1) параметри у Workspace

% Зібрати параметри у структуру P (зручно передати у функцію) -------------
P.g=g; P.m=m; P.Ix=Ix; P.Iy=Iy; P.Iz=Iz; P.Ip=Ip;
P.b=b; P.d=d; P.l=l; P.Omega0=Omega0; P.tau=tau;
P.Omega_min = Omega_min*2*pi;     % 0   об/с -> рад/с
P.Omega_max = Omega_max*2*pi;     % 150 об/с -> рад/с

% Початковий стан: висіння на висоті 10 м (z = -10 у NED) ------------------
x0        = zeros(16,1);
x0(3)     = -10;                  % z = -10 м  (10 м над землею)
x0(13:16) = Omega0;              % двигуни вже крутяться на обертах висіння

% 2) Ступінчасте збурення (Step) ------------------------------------------
A     = 1;                        % амплітуда ступеня            [рад/с]
tstep = 1;                        % момент увімкнення            [с]

% Оберіть канал збурення (зніміть коментар з ОДНОГО рядка) ----------------
mix = [ 1  1  1  1];  ch = 'Вертикаль (усі двигуни)';
% mix = [ 0  1  0 -1];  ch = 'Крен (двигуни 2 та 4)';
% mix = [ 1  0 -1  0];  ch = 'Тангаж (двигуни 1 та 3)';
% mix = [-1  1 -1  1];  ch = 'Рискання (пари 1,3 та 2,4)';

% Ступінчаста функція амплітудою A, що вмикається в момент tstep ----------
Ufun = @(t) A * (t>=tstep) * mix(:);

% 3) Інтегрування рівнянь (ode45) -----------------------------------------
odefun = @(t,x) quad_nonlinear(t, x, Ufun(t), P);
opts   = odeset('RelTol',1e-6,'AbsTol',1e-8,'MaxStep',0.01);
[t, X] = ode45(odefun, [0 10], x0, opts);

% 4) Графіки перехідних процесів ------------------------------------------
figure('Name',['Реакція на збурення: ' ch],'Color','w');

subplot(2,2,1);
plot(t, X(:,1), t, X(:,2), t, X(:,3),'LineWidth',1.2); grid on;
xlabel('час [с]'); ylabel('положення [м]');
legend('x','y','z (вниз; -10 = 10 м угору)','Location','best');
title('Положення (земна система NED)');

subplot(2,2,2);
plot(t, X(:,4), t, X(:,5), t, X(:,6),'LineWidth',1.2); grid on;
xlabel('час [с]'); ylabel('швидкість [м/с]');
legend('u','v','w','Location','best');
title('Швидкість (зв''язана система)');

subplot(2,2,3);
plot(t, rad2deg(X(:,7)), t, rad2deg(X(:,8)), t, rad2deg(X(:,9)),'LineWidth',1.2); grid on;
xlabel('час [с]'); ylabel('кут [град]');
legend('крен \phi','тангаж \theta','рискання \psi','Location','best');
title('Орієнтація (кути Ейлера)');

subplot(2,2,4);
plot(t, rad2deg(X(:,10)), t, rad2deg(X(:,11)), t, rad2deg(X(:,12)),'LineWidth',1.2); grid on;
xlabel('час [с]'); ylabel('кутова швидкість [град/с]');
legend('p','q','r','Location','best');
title('Кутові швидкості (зв''язана система)');

% Загальний заголовок (якщо MATLAB старий і sgtitle недоступний - видаліть)
sgtitle(['Ступінь амплітудою ' num2str(A) ' рад/с, канал: ' ch]);
